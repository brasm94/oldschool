        temp[1] = ffMultiply(state[0][c],0x09) ^ ffMultiply(state[1][c],0x0e) ^ ffMultiply(state[2][c],0x0b) ^ ffMultiply(state[3][c],0x0d);
        temp[2] = ffMultiply(state[0][c],0x0d) ^ ffMultiply(state[1][c],0x09) ^ ffMultiply(state[2][c],0x0e) ^ ffMultiply(state[3][c],0x0b);
        temp[3] = ffMultiply(state[0][c],0x0b) ^ ffMultiply(state[1][c],0x0d) ^ ffMultiply(state[2][c],0x09) ^ ffMultiply(state[3][c],0x0e);
        state[0][c] = temp[0];
        state[1][c] = temp[1];
        state[2][c] = temp[2];
