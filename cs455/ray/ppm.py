
import time
def write_2_ppm(arr,row,col,filename):
    header = "P3\n{} {}\n255\n".format(row,col)
    f = open(filename,"w+")
    f.write(header)
    for i,val in enumerate(arr):
        f.write(str(val))
        f.write(" ")
        if (i + 1) % (col * 3) == 0:
            f.write("\n")
